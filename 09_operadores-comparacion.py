# >
print('Mayor que')
print(7 > 3)
print(7 > 7)
print(3 > 7)

# <
print('Menor que')
print(7 < 3)
print(7 < 7)
print(3 < 7)

# >=
print('Mayor o igual que')
print(7 >= 3)
print(7 >= 7)
print(3 >= 7)

# <=
print('Menor o igual que')
print(7 <= 3)
print(7 <= 7)
print(3 <= 7)

# == extrictamente Igualdad
print("Igualdad")
print(6 == 6)
print(6 == 10)
print("Apple" == "Apple")
print("apple" == "Apple")

# != diferencia
print('Diferencia')
print(6 != 10)
print(6 != 6)